package com.day1;

public class ValueOfPrimitiveDataType {
	static boolean val1;
	static double val2;
	static float val3;
	static int val4;
	static long val5;
	static String val6;
	
	public static void main(String[] args) {
		System.out.println("Default values of primitive data types");  
		System.out.println("Val1 of boolean = " + val1);
	    System.out.println("Val2 of double = " + val2);
	    System.out.println("Val3 float = " + val3);
	    System.out.println("Val4 int = " + val4);
	    System.out.println("Val5 long = " + val5);
	    System.out.println("Val6 String = " + val6);
		
	}

}
